                    <div class="baslik"> <h1>Bilişim Teknolojileri Alanı</h1></div>

                    <p> Bilişim Teknolojileri Alanımız, okulumuzda ilk kez 2013-2014 eğitim öğretim yılında hizmet vermeye başlamıştır.  Anadolu Meslek Lisesi okul türünde sınıflarımız mevcuttur.</p>
                    <br>
                    <p>Bilişim Teknolojileri Alanı, güncel bir bakışla; bilgisayar donanımı, yazılımı, bilgisayar ağları, iletişim teknolojileri, bu alanda yetişmiş insan gücü, prosedürler, internet, intranet ve iletişim araçları gibi çok sayıda bileşene sahiptir. Bilişim sektörü dünyada son 50 yıldır var olan ancak günümüzde olağanüstü öneme sahip olan bir sektördür. Katma değeri oldukça yüksek olan bilişim sektörü, gelişmiş ülkelerde gözde sektörlerin başında gelmektedir.</p>
                    <br>
                    <div class="mezuniyet">

                        <h3> Mezuniyet Sonrası</h3>
                        <ul>
                            <li>Türkiye' de her türlü üniversitelerde okuyabilirsiniz.</li>
                            <br>
                            <li>Bilişim teknolojileri öğretmenliğine ek puan avantajı ile gidebilirsiniz.</li>
                            <br>
                            <li>Artık üniversitelerin Bilgisayar mühendisliğinde de okuyabilirsiniz.</li>
                            <br>
                            <li>Sınavsız olarak Meslek yüksek okullarına geçebilirsiniz.</li>
                            <br>
                            <li>Üniversitelerin iki  yıllık programlarını bitirdikten sonra dikey geçiş sınavıyla, mühendislik ya da öğretmenlik bölümlerinde eğitiminize devam edebilirsiniz.</li>
                            <br>
                        </ul>
                    </div>
